        <center><h3>Proses Pembuatan Surat Keterangan Pengantar</h3></center><br>
      
        <ol type="1">
			<li>Pemohon / masyarakat yang akan membuat surat pengantar keterangan harus verifikasi data terlebih dahulu 
				<i>(memilih menu "Layanan Surat"->"Buat surat"->"Jenis Surat")</i> dengan memasukan NIK/NO KTP yang berlaku 
				untuk validasi data yang menerangkan bahwa Pemohon/Masyarakat bersangkutan terdaftar sebagai warga Desa
			</li>
			<li>Setelah melakukan validasi NIK pemohon melakukan pendaftaran dan mencetak bukti registrasi
			</li>
			<li>Pemohon/Masyarakat mencetak Surat Keterangan setelah surat disetujui oleh petugas
				<i>(Persetujuan surat selambat-lambatnya 1x24 jam (1 hari))</i>
			</li>
			<li>
				Pemohon/Masyarakat mencetak Surat di Kantor Desa dengan menyerahkan bukti registrasi
			</li>	
